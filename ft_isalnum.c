#include "libft.h"

int	ft_isalnum(int num)
{
	if (('0' > num) || ('9' < num))
	{
		if (('a' > num) || ('z' < num))
		{
			if (('A' > num || 'Z' < num))
			{
				return (0);
			}
		}
	}
	return (1);
}
